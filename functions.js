// indexOf(value)
/*
    찾으려는 특정 문자 value를 찾아서 그 위치의 인덱스 값을 반환하는 함수
    여러문자가 있다면 첫번째 위치를 반환한다.
    찾으려는 문자가 없다면 -1을 반환한다.
*/

// includes(value)
/*
    찾으려는 특정 문자 value가 문자열 안에 있으면 true를 반환하고, 아니면 false를 반환한다.
*/

